 <h2> <u>OVERVIEW</u> </h2>
 <p>The word to guess is represented by a row of dashes, representing each letter of the word. Words you cannot use include proper nouns such as names, places, and brands. If the guessing player suggests a letter which occurs in the word, the other player writes it in all its correct positions. If the suggested letter or number does not occur in the word, the other player draws one element of a hanged man stick figure as a tally mark. The game is over when:
 <br>
The guessing player completes the word, or guesses the whole word correctly <br>
The other player completes the diagram
<br><br>
This diagram is, in fact, designed to look like a hanging man. Although debates have arisen about the questionable taste of this picture, it is still in use today. A common alternative for teachers is to draw an apple tree with ten apples, erasing or crossing out the apples as the guesses are used up.
<br><br>
The exact nature of the diagram differs; some players draw the gallows before play and draw parts of the man's body (traditionally the head, then the torso, then the arms and legs one by one). Some players begin with no diagram at all, and drawing the individual elements of the gallows as part of the game, effectively giving the guessing players more chances. The amount of detail on the man can also vary, affecting the number of chances. Some players include a face on the head, either all at once or one feature at a time.
<br><br>
Some modifications to game play to increase difficulty level are sometimes implemented, such as limiting guesses on high-frequency consonants and vowels. Another alternative is to give the definition of the word. This can be used to facilitate the learning of a foreign language.
</p>
<br><br><br>
<h2> <u> HISTORY </u> </h2>
<p> "The origins of Hangman are obscure meaning not discovered, but it seems to have arisen in Victorian times," says Tony Augarde, author of The Oxford Guide to Word Games. The game is mentioned in Alice Bertha Gomme's "Traditional Games" in 1894 under the name "Birds, Beasts and Fishes." The rules are simple; a player writes down the first and last letters of a word and another player guesses the letters in between. In other sources, the game is called "Gallows", "The Game of Hangin'", or "Hanger". Hangman has featured in the 1978 Speak & Spell video game system under the name "Mystery Word" and is sometimes played today on Internet forums.
</p>
<br><br><br>
<h2> <u> STRATEGY </u> </h2>
<p>In the English language, the twelve most commonly occurring letters are, in descending order: e-t-a-o-i-n-s-h-r-d-l-u. This and other letter-frequency lists are used by the guessing player to increase the odds when it is their turn to guess. On the other hand, the same lists can be used by the puzzle setter to stump their opponent by choosing a word which deliberately avoids common letters (e.g. rhythm or zephyr) or one that contains rare letters (e.g. jazz).
<br><br>
Another common strategy is to guess vowels first, as English only has five vowels (a, e, i, o, and u, while y may sometimes, but rarely, be used as a vowel) and almost every word has at least one.
<br><br>
According to a 2010 study conducted by Jon McLoone for Wolfram Research, the most difficult words to guess include jazz, buzz, hajj, faff, fizz, fuzz and variations of these.
</p>
